<div class="unit three-of-four">
	<h2>Help and FAQ's</h2>
    <h3>How do I rotate the map?</h3>
    <p>To rotate the campus map simply click the left <i class="fa fa-chevron-left"></i> or right <i class="fa fa-chevron-right"></i> buttons towards the bottom of the screen. This will rotate the map by a small amount in your chosen direction.</p>
    <h3>How do I change perspective?</h3>
    <p>The button to change perspective is found between the two rotation buttons at the bottom of the screen. This button will switch between three perspective types: the standard perspective view, a low perspective view and a birds-eye view.</p>
    <h3>Can I zoom in the map?</h3>
    <p>Yes the map can be zoomed in by scrolling the mouse wheel or pinching two fingers together on a touch device.</p>
    <h3>How do I display data on the map?</h3>
    <p>All data overlays can be toggled on or off via the sliding navigation to the left of the screen. Select one of the buttons in the left navigation to reveal a list of data (i.e. building types, bus routes, subject data) which can then be toggled to appear on the map.</p>
    <h3>Can I find a building or room easily?</h3>
    <p>Yes, any building or room can be found by using the search bar at the top of the screen. Start typing your query to reveal suggestions, click on one of the suggestions and you will be taking to the corresponding building. If searching for a room you will be taken to the building which includes the particular room. You can then select the room from the rooms list which will take you to the University of Kent website.</p>
    <h3>What does the magnifying glass icon do?</h3>
    <p>The magnifying glass icon will bring you into a first-person view of that specific area of the map. Once in this view you can click and drag the mouse to rotate the camera and view popular events and places around the campus.</p>
    <h3>What are the Twitter icons for?</h3>
    <p>The Twitter icons will display the most recent tweet from various areas of the campus when clicked.</p>
